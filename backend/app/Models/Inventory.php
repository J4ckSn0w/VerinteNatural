<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;
use Illuminate\Support\Facades\Auth;

class Inventory extends Model
{
    use HasFactory, SoftDeletes;

    protected $STATUS = [
        0 => 'Por encima del stock minimo',
        1 => 'Cerca del stock minimo',
        2 => 'Por debajo del stock minimo',
        3 => 'Sin existencias'
    ];

    protected $table = 'inventories';

    protected $fillable = [
        'available',
        'total',
        'product_name',
        'sku',
        'warehouse_id',
        'batch_id',
        'minium_stock'
    ];

    /*********** Methods ************/

    /********** End Methods *********/


    /*********** Relations ************/

    public function batch()
    {
        return $this->belongsTo(Batch::class);
    }

    public function warehouse()
    {
        return $this->belongsTo(Warehouse::class);
    }

    public function units()
    {
        return $this->belongsToMany(Unit::class)->withPivot('price', 'is_default');
    }

    /********** End Relations *********/


    /*********** Appends ************/

    public function getWarehouseNameAttribute()
    {
        return $this->warehouse->name ?? '';
    }

    public function getStatusNameAttribute()
    {
        return $this->STATUS[$this->status] ?? '';
    }

    public function getUnitDefaultAttribute()
    {
        $unit = $this->units()->select('id', 'name', 'price')->where('is_default', 1)->first() ??  null;
        if ($unit)
            return $unit->only('id', 'name', 'price');
        else return null;
    }

    public function getImageAttribute()
    {
        return config('services.storage.products') . substr($this->sku, 6, 12)  . '.png';
    }

    /********** End Appends *********/

    // BOOT
    public static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub

        static::created(function ($model) {
            Log::create([
                "category" => "Inventarios",
                "action" => "Se creó el inventario " . $model->sku,
                "user_id" => Auth::id()
            ]);
        });

        static::updated(function ($model) {

            Log::create([
                "category" => "Inventarios",
                "action" => "Se actualizó el inventario " . $model->sku,
                "user_id" => Auth::id()
            ]);
        });

        static::deleted(function ($model) {

            Log::create([
                "category" => "Inventarios",
                "action" => "Se eliminó el inventario " . $model->sku,
                "user_id" => Auth::id()
            ]);
        });
    }
}
